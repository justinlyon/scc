Installation
------------
Backup your Joomla installation and database before using. Install the component and the plugin. Make sure the plugin is published.

If you don't want the dump popup window to appear automatically, you can disable it in the configuration. To display the dump window manually:
- Administrator: Go to Components -> Dump and click Popup.
- Site: Make a new menu item for Dump. Set it to 'Open in New Window'
